package model;

public class Influencias {

	private Integer idInfluencia;
	private Integer idUsuario;
	private String humor;
	private String saude;
	private String horasDormidas;
	
	
	public Integer getIdInfluencia() {
		return idInfluencia;
	}
	public void setIdInfluencia(Integer idInfluencia) {
		this.idInfluencia = idInfluencia;
	}
	
	public Integer getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getHumor() {
		return humor;
	}
	public void setHumor(String humor) {
		this.humor = humor;
	}
	public String getSaude() {
		return saude;
	}
	public void setSaude(String saude) {
		this.saude = saude;
	}
	public String getHorasDormidas() {
		return horasDormidas;
	}
	public void setHorasDormidas(String horasDormidas) {
		this.horasDormidas = horasDormidas;
	}
	
	
}
